use anyhow::{Context, Result};
use std::process::Command;

pub struct PrGenerator;

impl PrGenerator {
    pub fn new() -> Self {
        Self
    }

    /// Generate a PR description for a dependency update
    pub fn generate(&self, dep_name: &str, old_version: &str, new_version: &str) -> String {
        let changelog_url = self.get_changelog_url(dep_name);

        format!(
            r#"## Dependency Update: {} {} → {}

This PR updates **{}** from version **{}** to **{}**.

### Links

- 📦 [crates.io](https://crates.io/crates/{})
- 📝 [Changelog/Releases]({})
- 🔍 [Diff](https://github.com/search?q=repo%3Aslawlor%2Factor+{}&type=commits)

### Changes

This is a **major version update** and may include breaking changes. Please review the changelog for migration notes.

### Version Bump

The `ractor_actors` version has been bumped accordingly to reflect this dependency update.

### Checklist

- [ ] Reviewed changelog for breaking changes
- [ ] Tests pass locally
- [ ] CI passes

---

*This PR was automatically generated by the dependency tracker workflow.*
"#,
            dep_name,
            old_version,
            new_version,
            dep_name,
            old_version,
            new_version,
            dep_name,
            changelog_url,
            dep_name
        )
    }

    /// Get the changelog URL for a dependency
    fn get_changelog_url(&self, dep_name: &str) -> String {
        match dep_name {
            "ractor" => "https://github.com/slawlor/ractor/releases".to_string(),
            "chrono" => "https://github.com/chronotope/chrono/blob/main/CHANGELOG.md".to_string(),
            "cron" => "https://github.com/zslayton/cron/releases".to_string(),
            "notify" => "https://github.com/notify-rs/notify/blob/main/CHANGELOG.md".to_string(),
            _ => format!("https://crates.io/crates/{}/versions", dep_name),
        }
    }

    /// Create a pull request using GitHub CLI
    pub fn create_pr(
        &self,
        dep_name: &str,
        new_version: &str,
        description: &str,
        labels: &[String],
    ) -> Result<()> {
        // Create branch
        let branch_name = format!("auto-deps/{}-{}", dep_name, new_version);

        // Check if branch already exists
        let branch_exists = Command::new("git")
            .args(["rev-parse", "--verify", &branch_name])
            .output()
            .map(|o| o.status.success())
            .unwrap_or(false);

        if branch_exists {
            println!(
                "  ⚠️  Branch {} already exists, checking if PR exists",
                branch_name
            );
            return Ok(());
        }

        // Create and checkout branch
        Command::new("git")
            .args(["checkout", "-b", &branch_name])
            .status()
            .context("Failed to create branch")?;

        // Stage changes
        Command::new("git")
            .args(["add", "ractor_actors/Cargo.toml", "Cargo.lock"])
            .status()
            .context("Failed to stage changes")?;

        // Commit
        let commit_msg = format!("deps: Update {} to {}", dep_name, new_version);
        Command::new("git")
            .args(["commit", "-m", &commit_msg])
            .status()
            .context("Failed to commit changes")?;

        // Push
        Command::new("git")
            .args(["push", "-u", "origin", &branch_name])
            .status()
            .context("Failed to push branch")?;

        // Create PR using gh CLI
        let title = format!("deps: Update {} to {}", dep_name, new_version);
        let mut args = vec![
            "pr",
            "create",
            "--title",
            &title,
            "--body",
            description,
            "--base",
            "main",
        ];

        // Add labels
        let labels_str = labels.join(",");
        if !labels.is_empty() {
            args.push("--label");
            args.push(&labels_str);
        }

        let output = Command::new("gh")
            .args(&args)
            .output()
            .context("Failed to create PR with gh CLI")?;

        if !output.status.success() {
            let stderr = String::from_utf8_lossy(&output.stderr);
            anyhow::bail!("gh pr create failed: {}", stderr);
        }

        // Return to original branch
        Command::new("git")
            .args(["checkout", "-"])
            .status()
            .context("Failed to return to original branch")?;

        Ok(())
    }
}
